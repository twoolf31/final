	<h3>Extra, Extra: The Colors Have Meaning</h3>

    <p class="intro">Beware, for this is where it gets tricky. Did you know that every color on every pride flag has its own distinct meaning based on which community the flags belongs?</p>
    <p>Some meanings are very interpretive: the purple in the standard pride flags represents spirit, while the green represents nature. But some meanings are a little easier to decipher: the purple of the bisexual flag represents an attraction to people of the other gender and of the same gender, while the green in the agender flag represents nonbinary because of how green is the inverse of purple (the color often associated with the mix of both male and female.</p>
    <p>And that's the other thing. Flag colors do not represent the same thing on each flag. Each flag (and each color in said flag) has its own meaning and interpretation based on the thoughts, feelings, and identities of those a part of that flag's community. It's not important to know the meaning of every single color in every different flag, however; it's only important to know that each color and flag has a distinct meaning and that they are all important and valid.</p>

